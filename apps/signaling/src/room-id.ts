/**
 * 生成「4 位数字-中文词-中文词」短口令，如 `8423-河马-火山`。
 * rng 默认 Math.random，可注入以便测试。
 */
export function generateRoomId(rng: () => number = Math.random): string {
  const digits = String(Math.floor(rng() * 10000)).padStart(4, '0');
  const word1 = WORDS[Math.floor(rng() * WORDS.length)];
  const word2 = WORDS[Math.floor(rng() * WORDS.length)];
  return `${digits}-${word1}-${word2}`;
}
